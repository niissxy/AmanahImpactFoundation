// Runtime cache for dynamic translations fetched from the server-side translation engine, persisted in localStorage
const CACHE_LOCAL_STORAGE_KEY = 'amanah_translation_cache_v2';
export const dynamicTranslationCache: Record<string, Record<string, string>> = (() => {
  try {
    const stored = localStorage.getItem(CACHE_LOCAL_STORAGE_KEY);
    const parsed = stored ? JSON.parse(stored) : {};
    
    // Clean up any values starting with simulated tags like [EN]
    for (const textKey of Object.keys(parsed)) {
      if (parsed[textKey] && typeof parsed[textKey] === 'object') {
        for (const langKey of Object.keys(parsed[textKey])) {
          let val = parsed[textKey][langKey];
          if (typeof val === 'string') {
            val = val.replace(/^\[[A-Z]{2}\]\s*/, '');
            parsed[textKey][langKey] = val;
          }
        }
      }
    }
    return parsed;
  } catch (e) {
    return {};
  }
})();

function saveCacheToLocalStorage() {
  try {
    localStorage.setItem(CACHE_LOCAL_STORAGE_KEY, JSON.stringify(dynamicTranslationCache));
  } catch (e) {
    console.error('Failed to save translation cache to localStorage:', e);
  }
}

const pendingRequests = new Set<string>();
const batchQueue: Map<string, Set<string>> = new Map(); // targetLang -> Set of texts
let batchTimeoutId: any = null;

async function dispatchBatches() {
  const currentBatches = Array.from(batchQueue.entries());
  batchQueue.clear();

  for (const [lang, textSet] of currentBatches) {
    const textsArray = Array.from(textSet).filter(text => {
      const pendingKey = `${text} ||| ${lang}`;
      if (pendingRequests.has(pendingKey)) return false;
      if (dynamicTranslationCache[text] && dynamicTranslationCache[text][lang]) return false;
      return true;
    });

    if (textsArray.length === 0) continue;

    // Mark as pending
    textsArray.forEach(text => {
      pendingRequests.add(`${text} ||| ${lang}`);
    });

    try {
      const response = await fetch('/api/translate-batch', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ texts: textsArray, targetLang: lang })
      });

      if (!response.ok) throw new Error('Failed to fetch batch translations');

      const data = await response.json();
      if (data && data.translations) {
        for (const [orig, trans] of Object.entries(data.translations)) {
          const trimKey = orig.trim();
          
          if (!dynamicTranslationCache[orig]) {
            dynamicTranslationCache[orig] = {};
          }
          dynamicTranslationCache[orig][lang] = String(trans);

          if (!dynamicTranslationCache[trimKey]) {
            dynamicTranslationCache[trimKey] = {};
          }
          dynamicTranslationCache[trimKey][lang] = String(trans);
        }
        
        saveCacheToLocalStorage();
        // Notify the React components to re-render and consume the new translations
        window.dispatchEvent(new CustomEvent('translation-loaded'));
      }
    } catch (err) {
      console.error(`Batch translation dispatch failed for lang ${lang}:`, err);
    } finally {
      // Remove from pending
      textsArray.forEach(text => {
        pendingRequests.delete(`${text} ||| ${lang}`);
      });
    }
  }
}

function queueTranslation(text: string, lang: string) {
  if (!batchQueue.has(lang)) {
    batchQueue.set(lang, new Set());
  }
  batchQueue.get(lang)!.add(text);

  if (batchTimeoutId) {
    clearTimeout(batchTimeoutId);
  }

  // Debounce dispatch for 100ms to group all components' translation demands together
  batchTimeoutId = setTimeout(() => {
    dispatchBatches();
  }, 100);
}

/**
 * Universal dynamic lookup function that translates any Indonesian string to currentLang.
 * If the string doesn't exist in the dictionary, it triggers a non-blocking background fetch
 * to the translation API and gracefully returns the original text in the meantime. Once loaded,
 * it raises a 'translation-loaded' event to trigger a reactive UI re-render.
 */
export function getTranslation(text: string, lang: string): string {
  if (!text) return '';
  if (lang === 'id') return text;
  
  const cleanKey = text.trim();

  // 1. Check in runtime dynamic cache first
  if (dynamicTranslationCache[cleanKey] && dynamicTranslationCache[cleanKey][lang]) {
    return dynamicTranslationCache[cleanKey][lang];
  }
  
  // 2. Check static phrase dictionary
  const matched = phraseDictionary[cleanKey];
  if (matched && matched[lang]) {
    return matched[lang];
  }
  
  // Try case-insensitive lookup
  const lowerKey = cleanKey.toLowerCase();
  for (const [k, dict] of Object.entries(phraseDictionary)) {
    if (k.toLowerCase() === lowerKey && dict[lang]) {
      return dict[lang];
    }
  }

  // Also lookup in the simple translations if exists
  const langKeys = translations[lang];
  const idKeys = translations['id'];
  if (langKeys && idKeys) {
    // Find the key associated with the ID text
    for (const [k, val] of Object.entries(idKeys)) {
      if (val === text && langKeys[k]) {
        return langKeys[k];
      }
    }
  }

  // 3. Initiate dynamic non-blocking background translation if not already pending/cached
  const pendingKey = `${cleanKey} ||| ${lang}`;
  if (!pendingRequests.has(pendingKey)) {
    queueTranslation(cleanKey, lang);
  }
  
  return text;
}
